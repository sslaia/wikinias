<p>Ya'ahowu. Ba da'a so ngawalö <strong>Nidunö-dunö</strong> ba li Niha.
So nidunö-dunö khö ndraono ba so göi khö zatua.</p>

<p>Nidunö-dunö khö ndraono to'ölö lafotöi ia ba li Indonesia <em>dongeng</em>, 
ba so mato ha'uga ngawua <em>dongeng</em> ba da'a tou</p>

<p>Nidunö-dunö khö zatua so mato ha'uga ngawalö: 
so nifotöi nidunö-dunö sadogo-dogo (<em>cerpen</em>),
so zabölö anau ma'ifu nifotöi <em>novella</em>,
ba afuriatania nidunö-dunö sanau nifotöi <em>novel</em>.</p>

